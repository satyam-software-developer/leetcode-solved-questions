class Solution {
	
	// Write the first function to
	int volume(int s) {
		// return the volume of cube
		return s * s * s;
	}
	
	// Write the second function to
	int volume(int l, int w, int h) {
		// return the volume of the rectangular box
		return l * w * h;
	}
}


